public class IfElseIfLadderStatement {
public static void main(String[] args) {
String day="Tuesday";
    if (day=="Monday") {
        System.out.println("this  is the First day of the week");
    }
    else if (day=="Tuesday") {
        System.out.println("this  is the Second day of the week");
    }
    else if (day=="Wednesday") {
        System.out.println("this  is the Third day of the week");
    }
    else  {
        System.out.println("this  is not the first ,second or third  day of the week");
    }
}
}
